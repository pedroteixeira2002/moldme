/// Represents the status of an entity.
enum Status {
  /// The entity is new.
  newEntity,

  /// The entity is in progress.
  inProgress,

  /// The entity is done.
  done,

  /// The entity is closed.
  closed,

  /// The entity is canceled.
  canceled,

  /// The entity is pending.
  pending,

  /// The entity is accepted.
  accepted,

  /// The entity is denied.
  denied,
}
